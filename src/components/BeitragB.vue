  name: 'Beitrag',
  data () {
    return {
      movies: [
        { title: 'To The Moon', date: '11. Juli 2024', imgSrc: '../assets/ToTheMoon.png' },
        { title: 'Borderlands', date: '22. August 2024', imgSrc: '../assets/Borderlands.png' },
